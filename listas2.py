#extend
lista = [1, 2]
lista.extend([3 , 4])
print(lista) #[1, 2, 3, 4]

#insert
lista = [0, 2]
lista.insert(2,"Casa")
print(lista)

#remove
lista = [1, 2, 3]
lista.remove(2)
print (lista)

#pop(ultima posicion o de acuerdo a cual se seleccione borrar)
lista = [1, 2, 3]
lista.pop()
print(lista) #[1, 2]

#reverse(cambia el orden de la lista)
lista = [1, 2, 3]
lista.reverse()
print(lista)  #[3, 2, 1]


